# Basic Calculations
# Calculator Program in Python by using input() and format() functions

#Promting input from the user

n1 = float(input("Enter the First Number: "))
n2 = float(input("Enter the Second Number: "))

#addition

print("{} + {} = ".format(n1, n2))
print(n1 + n2)

#subtraction

print("{} - {} = ".format(n1, n2))
print(n1 - n2)

#multiplication

print("{} * {} = ".format(n1, n2))
print(n1 * n2)

#division

print("{} / {} = ".format(n1, n2))
print(n1 / n2)